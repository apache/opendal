    def __getattr__(name: str) -> type:
        """Resolve every ``*Config`` (and ``ServiceConfig``) to ``dict`` at runtime.

        The typed shapes exist only for static checkers; at runtime a config is a
        plain ``dict``. This keeps the module importable with no dependency on
        ``typing_extensions``.
        """
        if name == "ServiceConfig" or name.endswith("Config"):
            return dict
        msg = f"module {__name__!r} has no attribute {name!r}"
        raise AttributeError(msg)


